package com.qa.day3;


public class Exercise1 {

    public static void loop1() {
        //printing 1-10 10 times
        System.out.println("Printing 1-10 ten times.");
        for(int i = 0; i <= 10; i++) {
            for(int j = 1; j <= 10; j++) {
                System.out.println(j);
            }

        }

    }

    public static void main(String[] args) {
        loop1();



    }
}
